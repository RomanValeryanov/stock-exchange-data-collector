CREATE TABLE STAGE_YAHOO_FXRATE (
     ID           VARCHAR(20),
     NAME         VARCHAR(20) NOT NULL,
     RATE         NUMERIC(18,4),
     DATE         DATE NOT NULL,
     TIME         TIME NOT NULL,
     ASK          NUMERIC(18,4),
     BID          NUMERIC(18,4),
     SYM_ID       NUMERIC(18),
     ACTIVE_FLAG       CHAR(1) DEFAULT 'Y' NOT NULL CHECK (ACTIVE_FLAG in ('Y','N') ),
     ACTIVE_REASON     VARCHAR(20),

     CONSTRAINT IPK_STAGE_YAHOO_FXRATE PRIMARY KEY (NAME, DATE, TIME, ACTIVE_FLAG)
);
